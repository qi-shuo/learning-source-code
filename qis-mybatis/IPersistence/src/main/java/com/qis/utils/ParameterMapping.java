/**
 * 参数映射的内容
 *
 * @author qishuo
 * @date 2021/1/24 6:13 下午
 */
public class ParameterMapping {

    private String content;

    public ParameterMapping(String content) {
        this.content = content;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
